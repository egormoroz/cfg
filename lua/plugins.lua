-- Telescope setup

require('telescope').setup {
  defaults = {
    mappings = {
      i = {
        ['<C-u>'] = false,
        ['<C-d>'] = false,
      },
    },
  },
}
--Add leader shortcuts
local opts = { noremap = true, silent = true }
local function set_keymap(key, cmd) vim.api.nvim_set_keymap('n', key, cmd, opts) end
set_keymap('<leader><space>', [[<cmd>lua require('telescope.builtin').buffers()<CR>]])
set_keymap('<leader>sf', [[<cmd>lua require('telescope.builtin').find_files({previewer = false})<CR>]])
set_keymap('<leader>sb', [[<cmd>lua require('telescope.builtin').current_buffer_fuzzy_find()<CR>]])
set_keymap('<leader>sh', [[<cmd>lua require('telescope.builtin').help_tags()<CR>]])
set_keymap('<leader>st', [[<cmd>lua require('telescope.builtin').tags()<CR>]])
set_keymap('<leader>sd', [[<cmd>lua require('telescope.builtin').grep_string()<CR>]])
set_keymap('<leader>sp', [[<cmd>lua require('telescope.builtin').live_grep()<CR>]])
set_keymap('<leader>so', [[<cmd>lua require('telescope.builtin').tags{ only_current_buffer = true }<CR>]])
set_keymap('<leader>?', [[<cmd>lua require('telescope.builtin').oldfiles()<CR>]])

-- end of telescope setup


